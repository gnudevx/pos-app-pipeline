def prepare_feature_branch_compat(repo_dir, branch_name, base_branch=BACKBONE):
    """DEPRECATED alias — base_branch param ignored, always uses BACKBONE."""
    return prepare_feature_branch(repo_dir, branch_name)

def abort_to_backbone(repo_dir: str) -> None:
    """
    Dọn dẹp khi dev fail — quay về backbone để task tiếp theo không bị nhiễm.
    Thay thế _back_to_develop() trong flow mới.
    KHÔNG xóa feature branch — orchestrator giữ lại để debug nếu cần.
    """
    ok, _ = run(f"checkout {BACKBONE}", repo_dir)
    if not ok:
        run("checkout develop", repo_dir)  # hard fallback
        
